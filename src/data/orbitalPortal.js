/** Localised zone name/description with graceful fallback. */
export function zoneText(zone, lang) {
  const ar = lang === 'ar'
  return {
    name: (ar ? zone.name_ar : zone.name_en) || zone.name_en,
    desc: (ar ? zone.desc_ar : zone.desc_en) || zone.desc_en || '',
  }
}


